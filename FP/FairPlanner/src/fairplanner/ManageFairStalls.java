    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (txtNoOfStalls.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "No.of Stalls is required");
        } else if (txtSize.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Size is required");
        } else if (txtPrice.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Price is required");
        } else if (txtFairStall.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Fair Stall is required");
        } else {
            FairstallsBean objFairstallsBean = new FairstallsBean();//value send to mysql from ui
            objFairstallsBean.setFair_stall_id(Integer.parseInt(txtFairStallID.getText()));
            objFairstallsBean.setFair_id(Integer.parseInt(txtFairId.getText()));
            objFairstallsBean.setNo_of_stalls(Integer.parseInt(txtNoOfStalls.getText()));
            objFairstallsBean.setSize(txtSize.getText());
            objFairstallsBean.setPrice(Double.parseDouble(txtPrice.getText()));
            objFairstallsBean.setFair_stall(txtFairStall.getText());
            objFairstallsBean.setStatus(ddlStatus.getSelectedItem().toString());
            String result = "";
            if (addUpdateFlag.equals("add")) {
                result = objDB.addRecord(objFairstallsBean);
            } else {
                result = objDB.updateRecord(objFairstallsBean);
            }
            if (result.equals("added")) {
                JOptionPane.showMessageDialog(this, "User added successfully");
                lstFairstalls = objDB.getAllFairstalls();
                count = lstFairstalls.size() - 1;
                showRecord(lstFairstalls.get(count));
                disable(false);
                disableButtons(true);
            } else if (result.equals("updated")) {
                JOptionPane.showMessageDialog(this, "User updated successfully");
                lstFairstalls = objDB.getAllFairstalls();
                disable(false);
                disableButtons(true);
            } else {
                JOptionPane.showMessageDialog(this, "Operation Failed");
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

