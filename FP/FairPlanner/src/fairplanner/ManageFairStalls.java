    private void filldropdowns() {
        ddlFair.addItem("Select");
        for (int i = 0; i < lstFairs.size(); i++) {
            ddlFair.addItem(lstFairs.get(i).getFair_name());
        }
    }

    private void showFairInfo(FairmasterBean obj) {
        txtFairId.setText(obj.getFair_id() + "");
        txtFairStartDate.setText(obj.getFair_start_date());
        txtFairEndDate.setText(obj.getFair_end_date());
        txtAddress.setText(obj.getAddress());
    }

    public void showRecord(FairstallsBean objFairstallsBean) {
        txtFairStallID.setText(String.valueOf(objFairstallsBean.getFair_stall_id()));
        txtNoOfStalls.setText(String.valueOf(objFairstallsBean.getNo_of_stalls()));
        txtSize.setText(String.valueOf(objFairstallsBean.getSize()));
        txtPrice.setText(String.valueOf(objFairstallsBean.getPrice()));
        txtFairStall.setText(objFairstallsBean.getFair_stall());
        ddlStatus.setSelectedItem(objFairstallsBean.getStatus());

    }

    void disable(boolean value) {
        txtNoOfStalls.setEditable(value);
        txtSize.setEditable(value);
        txtPrice.setEditable(value);
        txtFairStall.setEditable(value);
        ddlStatus.setEnabled(value);
    }

    void disableButtons(boolean value) {
        btnFirst.setEnabled(value);
        btnPrevious.setEnabled(value);
        btnNext.setEnabled(value);
        btnLast.setEnabled(value);
        btnUpdate.setEnabled(value);
        btnSave.setEnabled(!value);
    }

    void clear() {
        txtNoOfStalls.setText("");
        txtSize.setText("");
        txtPrice.setText("");
        txtFairStall.setText("");
    }

