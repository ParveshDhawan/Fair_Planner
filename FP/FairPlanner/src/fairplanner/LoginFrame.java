    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String un = txtUsername.getText();
        String pass = new String(txtPassword.getPassword());
        DBOperations objDB = new DBOperations();
        UsermasterBean objBean = objDB.authUser(un, pass);
        if (objBean == null) {
            JOptionPane.showMessageDialog(this, "Invalid Username and Password");
        } else if (objBean.getUserStatus().equalsIgnoreCase("active")) {
            setVisible(false);
            new MainFrame(objBean).setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Sorry,You are an Inactive user");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

