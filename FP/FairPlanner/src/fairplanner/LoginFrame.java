    private void btnRetrievePasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRetrievePasswordActionPerformed
        // TODO add your handling code here:
        String result = new DBOperations().forgotPassword(txtUsername.getText());
        if (result.equalsIgnoreCase("sent")) {
            JOptionPane.showMessageDialog(this, "Password Successfully sent to your Email-Id");
        } else {
            JOptionPane.showMessageDialog(this, "Failed While Recovering Password Check Internet Settings");

        }
    }//GEN-LAST:event_btnRetrievePasswordActionPerformed

