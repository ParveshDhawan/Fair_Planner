    public void showRecord(FairtypeBean objFairtypeBean)
    {
        txtFairTypeID.setText(String.valueOf(objFairtypeBean.getFair_type_id()));
        txtFairType.setText(objFairtypeBean.getFair_type());
        txtDescription.setText(objFairtypeBean.getDescription());
        ddlStatus.setSelectedItem(objFairtypeBean.getStatus());
        
    }
    void disable(boolean value)
    {
        txtFairType.setEditable(value);
        txtDescription.setEditable(value);
        ddlStatus.setEnabled(value);
    }
    void disableButtons(boolean value)
    {
        btnFirst.setEnabled(value);
        btnPrevious.setEnabled(value);
        btnNext.setEnabled(value);
        btnLast.setEnabled(value);
        btnUpdate.setEnabled(value);
        btnSave.setEnabled(!value);
    }
