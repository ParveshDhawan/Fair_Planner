    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
         if(txtFairType.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"FairType is required");
        }
        else if(txtDescription.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"Description is required");
        }
        else
        {
            FairtypeBean objBean=new FairtypeBean();//value send to mysql from ui
            objBean.setFair_type_id(Integer.parseInt(txtFairTypeID.getText()));
            objBean.setDescription(txtDescription.getText());
            objBean.setStatus(ddlStatus.getSelectedItem().toString());
            String result="";
            if(addUpdateFlag.equals("add"))
            {
                result=objDB.addRecord(objBean);
            }
            else
            {
                result=objDB.updateRecord(objBean);
            }
            if(result.equals("added"))
            {
                JOptionPane.showMessageDialog(this,"User added successfully");
                lstFairtype=objDB.getAllFairTypes();
                count=lstFairtype.size()-1;
                showRecord(lstFairtype.get(count));
                disable(false);
                disableButtons(true);
            }
            else if(result.equals("updated"))
            {
                JOptionPane.showMessageDialog(this,"User updated successfully");
                lstFairtype=objDB.getAllFairTypes();
                disable(false);
                disableButtons(true);
            }
            else 
            {
                JOptionPane.showMessageDialog(this,"Operation Failed");
            }
            
        }    
    }//GEN-LAST:event_btnSaveActionPerformed

