    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(txtFairStartDate.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"Fair Start Date is required");
        }
        else if(txtFairEndDate.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"Fair End Date is required");
        }
        else if(txtFairName.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"Fair Name is required");
        }
        else if(txtDescription.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"Description is required");
        }
        else if(txtCity.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"City is required");
        }
        else if(txtAddress.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"Address is required");
        }
        else if(txtState.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"State is required");
        }
         else if(txtCountary.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this,"Countary is required");
        }
        else
        {
            FairmasterBean objFairmasterBean=new FairmasterBean();
            objFairmasterBean.setFair_id(Integer.parseInt(txtFairID.getText()));
            objFairmasterBean.setFair_start_date(txtFairStartDate.getText());
            objFairmasterBean.setFair_end_date(txtFairEndDate.getText());
            objFairmasterBean.setFair_name(txtFairName.getText());
            objFairmasterBean.setDescription(txtDescription.getText());
            objFairmasterBean.setAddress(txtAddress.getText());
            objFairmasterBean.setCity(txtAddress.getText());
            objFairmasterBean.setFair_type_id(lstfairTypes.get(ddlFairType.getSelectedIndex()-1).getFair_type_id());
            objFairmasterBean.setState(txtAddress.getText());
            objFairmasterBean.setCountary(txtCountary.getText());
            objFairmasterBean.setStatus(ddlStatus.getSelectedItem().toString());
            String result="";
            if(addUpdateFlag.equals("add"))
            {
                result=objDB.addRecord(objFairmasterBean);
            }
            else
            {
                result=objDB.updateRecord(objFairmasterBean);
            }
            if(result.equals("added"))
            {
                JOptionPane.showMessageDialog(this,"User added successfully");
                lstFairmaster=objDB.getAllFairmasters();
                count=lstFairmaster.size()-1;
                showRecord(lstFairmaster.get(count));
                disable(false);
                disableButtons(true);
            }
            else if(result.equals("updated"))
            {
                JOptionPane.showMessageDialog(this,"User updated successfully");
                lstFairmaster=objDB.getAllFairmasters();
                disable(false);
                disableButtons(true);
            }
            else 
            {
                JOptionPane.showMessageDialog(this,"Operation Failed");
            }
            
        }   
    }//GEN-LAST:event_btnSaveActionPerformed

