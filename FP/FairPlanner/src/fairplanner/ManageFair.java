    private void fillDropDowns()
    {
        ddlFairType.removeAll();
        ddlFairType.addItem("Select");
        for(int i=0;i<lstfairTypes.size();i++)
        {
            ddlFairType.addItem(lstfairTypes.get(i).getFair_type());
        }
    }
    public void showRecord(FairmasterBean objFairmasterBean)
    {
        txtFairID.setText(String.valueOf(objFairmasterBean.getFair_id()));
        txtFairStartDate.setText(String.valueOf(objFairmasterBean.getFair_start_date()));
        txtFairEndDate.setText(String.valueOf(objFairmasterBean.getFair_end_date()));
        txtFairName.setText(String.valueOf(objFairmasterBean.getFair_name()));
        txtDescription.setText(String.valueOf(objFairmasterBean.getDescription()));
        txtAddress.setText(String.valueOf(objFairmasterBean.getAddress())); 
        txtCity.setText(String.valueOf(objFairmasterBean.getCity()));
        txtState.setText(String.valueOf(objFairmasterBean.getState()));
        txtCountary.setText(String.valueOf(objFairmasterBean.getCountary()));
        ddlStatus.setSelectedItem(objFairmasterBean.getStatus());
    }
